import path from 'path'
import fs from 'fs-extra'
import { OUTPUT_DIR } from '../utils/paths'

export async function generateTypes() {
  const typesDir = path.resolve(OUTPUT_DIR, 'es')
  await fs.ensureDir(typesDir)

  // eslint-disable-next-line no-console
  console.log('Type definitions will be generated by TypeScript compiler')
  // eslint-disable-next-line no-console
  console.log('Run: pnpm typecheck to generate type definitions')
}
